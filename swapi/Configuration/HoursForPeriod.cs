namespace Swapi.Configuration
{
    /// <summary>
    /// Class that contains the hours of a time period.
    /// </summary>
    public class HoursForPeriod
    {
        /// <summary>
        /// The number of hours in a hour
        /// </summary>
        public const int Hour = 1;

        /// <summary>
        /// The number of hours in a day
        /// </summary>
        public const int Day = 24;

        /// <summary>
        /// The number of hours in a week (24* 7)
        /// </summary>
        public const int Week = 168;

        /// <summary>
        /// The number of hours in a month (24* 30)
        /// </summary>
        public const int Month = 720;

        /// <summary>
        /// The number of hours in a year (24 * 365)
        /// </summary>
        public const int Year = 8760;
    }
}
